    func renderer(_ renderer: SCNSceneRenderer, didAdd node: SCNNode, for anchor: ARAnchor) {
        guard let planeAnchor = anchor as? ARPlaneAnchor else { return }
            debugPrint(planeAnchor)
        addPlane(anchor: planeAnchor)
        }
    
    
